// Manipulando Strings e Números

// Transformar um número quebrado com 2 casas decimais e trocar ponto por vírgula

let number = 123456.654321
console.log(number.toFixed(2).replace(".", ",")) // No () coloque a quantidade de casas decimais desejadas.
// quando uma função é atrelada a um objeto(number), ela pode ser chamada de metodo.
// colocando um ponto, se pode atrelar mais função, como foi feito acima, com o replace